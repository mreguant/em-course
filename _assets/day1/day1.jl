# ╔═╡ f6d3813c-a11f-48ee-9212-15a67c71cf4b
begin
	close("all")
	regplot(x="wind_forecast", y="emis_tCO2", data=df, x_bins=10)
	title("Wind reduces emissions")
	xlabel("Wind forecast (GWh)")
	ylabel("Hourly emissions (tons CO2)")
	gcf()
end

# ╔═╡ e0e57a26-ea5f-47ad-9d13-5492cf5af1c5
begin
	close("all")
	regplot(x="wind_forecast", y="wholesale_price", data=df, x_bins=10)
	title("Wind reduces wholesale prices")
	xlabel("Wind forecast (GWh)")
	ylabel("Wholesale price (EUR/MWh)")
	gcf()
end

# ╔═╡ 68429700-c94c-45f6-9765-53478aaed243
begin
	close("all")
	regplot(x="wind_forecast", y="system_costs", data=df, x_bins=10)
	title("Wind increases congestion and reliability costs")
	xlabel("Wind forecast (GWh)")
	ylabel("System costs (EUR/MWh)")
	gcf()
end

# ╔═╡ f3f66300-a9b4-4a84-8281-d83820b098da
begin
	df["full_price"] = df.wholesale_price + df.system_costs
	close("all")
	regplot(x="wind_forecast", y="full_price", data=df, x_bins=10)
	title("But still reduces overall costs")
	xlabel("Wind forecast (GWh)")
	ylabel("Wholesale price + system costs (EUR/MWh)")
	gcf()
end

