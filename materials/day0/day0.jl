	# we clean up the data
	country_list = ["Germany", "Spain", "United States"];
	df_sample = df[df.Year .> 2000, :];
	filter!(row -> row.Entity in country_list, df_sample);
	select!(df_sample, ["Entity", "Year", "WindCapacity"]);
