# ╔═╡ 7293aa95-e8b4-4b38-8987-d2364a578c66
begin
	mydata = CSV.read("data_rtp.csv", DataFrame)
	mydata = dropmissing(mydata)
	first(mydata, 5)
end

# ╔═╡ 37a3544c-84c7-4a89-97b8-4c9b88e0d991
md"""

We create some Fourier transforms of time.

"""

# ╔═╡ 2cd44e61-31ca-471c-b13a-a66aa29472ba
begin
	mydata.t = mydata.date + mydata.hr/24;
	for x in [1, 7, 365]
		mydata[!, string("tau",x)] = ((mydata.t .+ 0.5)/x) * π * 2.0
		mydata[!, string("tau2",x)] = mydata[!, string("tau",x)].^2.0
		for k = 1:4
			mydata[!, string("cos",k,"tau",x)] = cos.(mydata[!, string("tau",x)]/k)
		end
	end
	select!(mydata,Not(:t));
	first(mydata, 5)
end

# ╔═╡ 796bb3d9-2fc5-4335-ae81-5b8e8ce0711c
md"""

We rescale variables to avoid problems with Lasso, which is also sensitive to scaling!

"""

# ╔═╡ 72302eae-5f8e-4b7c-b7c9-4d704d888481
begin
	# we rescale some variables as lasso can go "bananas"
	for v in names(mydata[!,Between(:wind_hat,:cos4tau365)])
		mu = mean(mydata[!,v]);
		sigma = std(mydata[!,v]);
		mydata[!,v] = (mydata[!,v] .- mu)/sigma
	end
	first(mydata, 5)
end

